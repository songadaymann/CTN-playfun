    introduceTaxMonster() {
        console.log("SceneOne: Starting tax monster intro sequence...");
        // if (this.gameState !== 'animating') return; // This check is no longer relevant here

        // Clean up elements from ThreeMillionScene (they are not in this scene anymore)
        // if (this.infoText) this.infoText.destroy(); // MOVED
        // if (this.funnel) this.funnel.destroy(); // MOVED
        // if (this.coinEmitter) this.coinEmitter.destroy(); // MOVED
        // Counter text remains and is managed by this scene

        // Start Tax Monster Sequence
        this.gameState = 'tax_intro'; // New state for this sequence

        // Immediately shake the screen and spawn the Tax Monster (no warning text or delay)
        this.cameras.main.shake(500, 0.02);

        // --- Spawn Tax Monster (off-screen left) ---
        const currentGroundTopY = this.scale.height - this.groundTileSize;

        // Spawn off-screen left; Y will be corrected after we know its real display height.
        const monsterStartX = -160; // arbitrary off-screen value; X corrected by tween later
        const monsterTempY  = 0;    // placeholder Y

        // Spawn Enemy without custom body so it auto-sizes.
        this.taxMonster = new Enemy(this, monsterStartX, monsterTempY, 'tax-monster_atlas');

        // Now that we know the scaled display height, place its bottom exactly on the ground.
        const monsterDisplayH = this.taxMonster.displayHeight;      // already includes scale
        const monsterStartY   = currentGroundTopY - monsterDisplayH / 2;
        this.taxMonster.setY(monsterStartY);
        // Make sure the physics body is synced with the new position
        this.taxMonster.body?.reset(this.taxMonster.x, monsterStartY);

        // Pixel-perfect snap using the physics body bottom
        const bodyBottom = this.taxMonster.body?.bottom ?? this.taxMonster.getBounds().bottom;
        const deltaY = currentGroundTopY - bodyBottom;
        if (deltaY !== 0) {
            this.taxMonster.y += deltaY;
            this.taxMonster.body?.reset(this.taxMonster.x, this.taxMonster.y);
        }

        // Extra snap using sprite bounds (accounts for trimmed frame)
        const spriteBottom = this.taxMonster.getBounds().bottom;
        const deltaSprite  = currentGroundTopY - spriteBottom;
        if (deltaSprite !== 0) {
            this.taxMonster.y += deltaSprite;
            this.taxMonster.body?.reset(this.taxMonster.x, this.taxMonster.y);
        }

        // Collider with Player (ensure it's added once)
        this.physics.add.collider(this.player, this.taxMonster, this.handlePlayerMonsterCollision, null, this);

        // --- Tween Monster onto Screen ---
        this.tweens.add({
            targets: this.taxMonster,
            x: 160,
            duration: 1500,
            ease: 'Power2',
            onStart: () => {
                // Play monster entrance sound when tween begins
                const soundManager = this.registry.get('soundManager');
                if (soundManager) {
                    soundManager.playSound('monsterEntranceSound', { volume: 0.1 });
                } else {
                    console.warn('SceneOne: SoundManager not found. Cannot play monster entrance sound.');
                }
            },
            onComplete: () => {
                console.log("Tax monster arrived. Game state changing to playing.");
                // Transition to Playing State AFTER monster arrives
                this.gameState = 'playing';
                this.cameras.main.startFollow(this.player, true, 0.1, 0.1);
                // Ensure world bounds are fully expanded for playing state
                this.physics.world.setBounds(0, 0, this.physics.world.bounds.width, this.physics.world.bounds.height);

                // --- Start Spawning Agents via Manager ---
                this.enemyManager.startSpawningAgents();

                // Allow the monster to push the player after arriving
                this.taxMonster.setImmovable(false);
                this.taxMonster.body.setMass(1000); // Make monster heavy so it pushes player
                this.taxMonster.setDepth(5);
            }
        });
    }


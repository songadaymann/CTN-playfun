        // --- Add Parallax Backgrounds (single wide images) ---
        this.bgFar = this.add.image(0, 0, 'scene1_bg_far')
            .setOrigin(0, 0)
            .setDepth(-10)
            .setScrollFactor(0.2);
        this.bgMid = this.add.image(0, 0, 'scene1_bg_mid')
            .setOrigin(0, 0)
            .setDepth(-9)
            .setScrollFactor(0.5);
        // NEW: Close / near layer for extra parallax depth
